public class DVDPlayer {

    public void on(){
        System.out.println("DVD Player ligado!");
    }

    public void off(){
        System.out.println("DVD Player desligado!");
    }

    public void play(){
        System.out.println("Iniciando reprodução de disco");
    }

    public void stop(){
        System.out.println("Pausando reprodução de disco");
    }

    public void eject(){
        System.out.println("Ejetando disco!");
    }
}
